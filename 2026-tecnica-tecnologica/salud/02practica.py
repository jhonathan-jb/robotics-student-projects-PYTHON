# Tipos de datos

# Datos de tipo entero (int)
edad = 17

# Datos de tipo flotante (float)
altura = 1.6  # metros

# Datos de tipo cadena de texto (str)
nombre = "mi nombre" 

# Datos de tipo booleano (bool)
esto_es_un_numero = False   # Falso
esto_es_otro_numero = True  # Verdadero

# Funcion type Sirve para ver los tipos de datos
tipo_de_dato_1 = type(edad)
print(tipo_de_dato_1)

tipo_de_dato_2 = type(altura)
print(tipo_de_dato_2)

tipo_de_dato_3 = type(nombre)
print(tipo_de_dato_3)

tipo_de_dato_4 = type(esto_es_un_numero)
print(tipo_de_dato_4)

# Funcion input
usuario = input("Introduce tu usuario")
#Ahora veremos que se guardo en la variable usuario
print(usuario)
#Veré el tipo de dato que es usuario
print(type(usuario))
